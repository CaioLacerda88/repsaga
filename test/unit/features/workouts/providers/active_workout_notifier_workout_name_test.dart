/// Family 6 (AW-EX-F-BR1-02) — `_generateWorkoutName` locale leak.
///
/// Pre-PR: the date format was hard-coded to `DateFormat('EEE MMM d')` (en
/// only) and the prefix was the literal `'Workout — '`. A pt-BR session
/// that started without a name was saved as e.g. `'Workout — Wed May 7'`,
/// which then displayed forever in pt-BR sessions.
///
/// Post-PR: locale is read at GENERATION TIME via
/// `ref.read(localeProvider).languageCode`. The prefix flows through
/// `l10n.workoutDefaultName(date)` and `DateFormat` is instantiated with
/// that language code so the date string honors locale conventions
/// (e.g. pt: `'Treino — qua 7 mai'`). The generated name is persisted
/// once and never re-rendered — matching the inline rationale at
/// `active_workout_notifier.dart:272-277`.
